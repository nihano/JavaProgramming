package day04_Concatenation;

public class Personal_Info {
    /*
    1. Create a class named PersonalInfo, and declare the variables with the appropriate for the followings:
            1. first name
            2. last name
            3. gender
            4. age
            5. from
            6. favorite music
            7. favorite book
            8. favorite show
            9. favorite color
            10. married status
     */
    public static void main(String[] args) {
        String first_name = "Nihan",
                last_name = "Oztekin",
                from = "Izmit",
                favourite_music = "\"Heavy Metal\"",
                favourite_book = "\"Marti\"",
                favourite_show = "\"Seinfeld\"",
                favourite_color = "Black";
        boolean isMarried = true,
                isEmployed = false;
        char gender = 'f';
        int age = 15;

        System.out.println("first_name = " + first_name);
        System.out.println("last_name = " + last_name);
        System.out.println("favourite_color = " + favourite_color);
        System.out.println("favourite_show = " + favourite_show);
        System.out.println("favourite_book = " + favourite_book);
        System.out.println("isMarried = " + isMarried);
        System.out.println("favourite_music = " + favourite_music);
        System.out.println("from = " + from);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("isEmployed = " + isEmployed);
/*
if a data type used more than once u can use comma (,)

not: you can use digits in variable name except the beginning
not: java is case-sensitive language
not: variable names should start with lower case

 */




    }



}
