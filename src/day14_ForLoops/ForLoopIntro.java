package day14_ForLoops;

public class ForLoopIntro {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        //when we know number of iteration we use for loop

        System.out.println("----------------------------------");

        for(int i = 1; i<10; i++){//i:1,2,3,4,5 ....10
            System.out.println("Hello World");//1,2,3,4,5....9 ==>9 times if we use i<=10 it will be executed 10 times
            //as long as i is less than 10 loop will be repeated
            //if you dont give the iteration it is infinite loop


        }




    }
}
