package day16_NestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");

        }

        System.out.println("---------------------");

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {// when the first for loop repeated one time this will repeat 5 times

                System.out.println("Wooden Spoon");
            }

        }



    }
}