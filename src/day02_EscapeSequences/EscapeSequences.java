package day02_EscapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Students\nToday we have Java Classes");// \n sequence adds a new line

        System.out.println("--------------------------------------------");

        System.out.println("\tJava Programming Language"); // \t adds paragraph space
        System.out.println("\t\t\tJava Developer Course Batch 1");

        System.out.println("--------------------------------------------");

        System.out.println("\\");

        System.out.println("--------------------------------------------");

        System.out.println("\"Seinfeld\"");
    }

}
/*
    1. \n: New Line
    2. \t: Paragraph Space
    3. \\: Single Backslash \
    4. \": Double Quote ""
 */