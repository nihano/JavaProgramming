package day06_ifStatements;

public class IfAndElseStatementsIntro {
    public static void main(String[] args) {
        int age =28;

        if (age >=21){
            System.out.println("eligible");
        }

        if (age < 21){
            System.out.println("not eligible");
        }
        System.out.println("------------------------");

        if (age >=21){
            System.out.println("eligible");
        }else{
            System.out.println("not eligible");
        }









    }
}
